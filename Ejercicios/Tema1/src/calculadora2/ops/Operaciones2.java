package calculadora2.ops;

public class Operaciones2 {

    public int suma(int op1, int op2) {
        return op1 + op2;
    }

    public int resta(int op1, int op2) {
        return op1 - op2;
    }

    public int multiplicacion(int op1, int op2) {
        return op1 * op2;
    }

    public int division(int op1, int op2) {
        return op1 / op2;
    }

}
