public class Ejercicio3 {

    public static void main(String[] args) {

        int initialValue = 100;
        System.out.println("Valor inicial: " + initialValue);
        initialValue += 50;
        System.out.println("Después de sumar 50 (+=): " + initialValue);
        initialValue -= 30;
        System.out.println("Después de restar 30 (-=): " + initialValue);
        initialValue *= 2;
        System.out.println("Después de multiplicar por 2 (*=): " + initialValue);
        initialValue /= 4;
        System.out.println("Después de dividir entre 4 (/=): " + initialValue);

    }

}
